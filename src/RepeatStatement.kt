
fun main() {

    /*
    repeat() представляет собой удобный способ повторения блока кода заданное количество раз.
    Он позволяет выполнять определенный блок кода несколько раз без явного использования циклов.
    */

    val times = 5
    repeat(times) {
        // Блок кода, который нужно повторить times раз
    }

    val n = 5
    repeat(n) {
        println("Hello, Kotlin!")
    }

}